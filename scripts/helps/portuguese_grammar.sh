alface() {
echo "
# “O alface” ou “a alface”?

Contudo, essa regra não se aplica à palavra alface, que tem gênero feminino definido.
Portanto, a maneira correta de escrever ou falar é “a alface”.

Alface é um substantivo feminino, logo, diz-se a alface. A dúvida acerca do gênero dessa palavra surge, sobretudo,
porque ela termina em “e”, não possuindo vogal final que marque o feminino (a) ou o masculino (o).

Na língua portuguesa existem muitas palavras terminadas em -e que apresentam a mesma forma no masculino e no feminino,
como o cliente/a cliente, o agente/a agente, o estudante/a estudante,… Contudo, essa regra não se aplica à palavra alface,
que tem gênero feminino definido. A utilização da palavra alface no masculino também sofre influência da designação
do conjunto das suas folhas, o pé de alface.

Para não errar, fique atento aos exemplos a seguir:

   > Esta alface está muito bonita.
   > Este pé de alface parece ótimo.

Adaptado de: http://duvidas.dicio.com.br/o-alface-ou-a-alface/
" | less -R -i
}

